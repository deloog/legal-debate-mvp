/**
 * 检查是否为重要法律
 */
export function isImportantLaw(lawName: string): boolean {
  return IMPORTANT_LAWS.some(
    importantLaw =>
      lawName.includes(importantLaw) || importantLaw.includes(lawName)
  );
}

/**
 * 获取法律的重要性级别
 */
export function getLawImportance(
  lawName: string
): 'critical' | 'important' | 'normal' {
  // 宪法和民法典为最高级别
  if (lawName.includes('宪法') || lawName.includes('民法典')) {
    return 'critical';
  }

  // 其他重要法律
  if (isImportantLaw(lawName)) {
    return 'important';
  }

  // 普通法律
  return 'normal';
}
