/**
 * 正则模式库
 * 定义用于检测法条关系的正则表达式
 */

/**
 * 引用关系模式
 * 用于检测法条中明确引用其他法条的情况
 */
export const CITE_PATTERNS = [
  // 根据《法律名》第X条
  /根据《([^》]+)》第?(\d+)条/g,
  // 依照《法律名》第X条
  /依照《([^》]+)》第?(\d+)条/g,
  // 按照《法律名》第X条
  /按照《([^》]+)》第?(\d+)条/g,
  // 参照《法律名》第X条
  /参照《([^》]+)》第?(\d+)条/g,
  // 适用《法律名》第X条
  /适用《([^》]+)》第?(\d+)条/g,
  // 引用《法律名》第X条
  /引用《([^》]+)》第?(\d+)条/g,
  // 依据《法律名》第X条
  /依据《([^》]+)》第?(\d+)条/g,
  // 遵照《法律名》第X条
  /遵照《([^》]+)》第?(\d+)条/g,
  // 没有书名号的情况：根据 法律名 第X条（限制法律名长度）
  /根据([^\s《》]{2,10})第(\d+)条/g,
  /依照([^\s《》]{2,10})第(\d+)条/g,
  /按照([^\s《》]{2,10})第(\d+)条/g,
  /参照([^\s《》]{2,10})第(\d+)条/g,
  /适用([^\s《》]{2,10})第(\d+)条/g,
] as const;

/**
 * 层级关系模式
 * 用于检测实施细则与上位法的关系
 */
export const HIERARCHY_PATTERNS = [
  // 根据《法律名》制定
  /根据《([^》]+)》制定/g,
  // 依据《法律名》制定
  /依据《([^》]+)》制定/g,
  // 为实施《法律名》
  /为实施《([^》]+)》/g,
  // 为执行《法律名》
  /为执行《([^》]+)》/g,
  // 根据《法律名》，制定本
  /根据《([^》]+)》[，,]制定本/g,
  // 没有书名号的情况
  /根据([^\s《》]{2,10})制定/g,
  /依据([^\s《》]{2,10})制定/g,
  /为实施([^\s《》]{2,10})/g,
  /为执行([^\s《》]{2,10})/g,
] as const;

/**
 * 冲突关系模式
 * 用于检测法条之间的冲突
 */
export const CONFLICT_PATTERNS = [
  // 与《法律名》规定不一致
  /与《([^》]+)》规定不一致/g,
  // 与《法律名》相抵触
  /与《([^》]+)》相抵触/g,
  // 与《法律名》冲突
  /与《([^》]+)》冲突/g,
  // 与《法律名》矛盾
  /与《([^》]+)》矛盾/g,
  // 没有书名号的情况
  /与([^\s《》]{2,10})规定不一致/g,
  /与([^\s《》]{2,10})相抵触/g,
  /与([^\s《》]{2,10})冲突/g,
  /与([^\s《》]{2,10})矛盾/g,
] as const;

/**
 * 替代关系模式
 * 用于检测新法替代旧法的情况
 */
export const SUPERSEDE_PATTERNS = [
  // 本法自X年X月X日起施行，《旧法》同时废止
  /本[法律规定]+自.+施行[，,][《]?([^》]+)[》]?同时废止/g,
  // 《旧法》自本法施行之日起废止
  /[《]?([^》]+)[》]?自本[法律规定]+施行之日起废止/g,
  // 原《旧法》废止
  /原[《]?([^》]+)[》]?废止/g,
] as const;

/**
 * 补全关系模式
 * 用于检测补充性规定
 */
export const COMPLETE_PATTERNS = [
  // 对《法律名》的补充规定
  /对[《]?([^》]+)[》]?的补充规定/g,
  // 补充《法律名》
  /补充[《]?([^》]+)[》]?/g,
  // 《法律名》的补充
  /[《]?([^》]+)[》]?的补充/g,
] as const;

/**
 * 法律名称提取模式
 * 用于从文本中提取法律名称
 */
export const LAW_NAME_PATTERN = /[《]([^》]+)[》]/g;

/**
 * 条款号提取模式
 * 用于从文本中提取条款号
 */
export const ARTICLE_NUMBER_PATTERN = /第(\d+)条/g;

/**
 * 所有模式的集合（用于测试）
 */
export const ALL_PATTERNS = {
  CITE_PATTERNS,
  HIERARCHY_PATTERNS,
  CONFLICT_PATTERNS,
  SUPERSEDE_PATTERNS,
  COMPLETE_PATTERNS,
  LAW_NAME_PATTERN,
  ARTICLE_NUMBER_PATTERN,
} as const;
